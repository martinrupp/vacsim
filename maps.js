function getMapStr(x)
{
  if( x === "0" )
    return mapStr0;
  if( x === "1" )
    return mapStr1;
  else if( x === "2" )
    return mapStr2;
  else if( x === "3" )
    return mapStr3;
  if( x === "4" )
    return mapStr5;
}

function getMapSize(x)
{
  if( x === "0" )
    return 324;
  else if( x === "1" )
    return 278;
  else if( x === "2" )
    return 264;
  else if( x === "3" )
    return 1070;
  if( x === "4" )
    return 1885;
}